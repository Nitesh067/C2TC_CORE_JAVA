package com.stringmethod;

public class StartsWith {

	public static void main(String[] args) {
		String s="Nitesh Kumar";
		String s1="Abhishek";
		String s2="   lol ";
		System.out.println(s.startsWith("N"));
		System.out.println(s1.startsWith(" "));
		System.out.println(s2.startsWith("lol"));

	}

}
