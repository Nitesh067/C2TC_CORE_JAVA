package com.stringmethod;

public class EndsWith {

	public static void main(String[] args) {
		String s = "Nitesh";
		String s1="Kumar0";
		String s2="Lol ";
		System.out.println(s.endsWith("h"));
		System.out.println(s1.endsWith("0"));
		System.out.println(s2.endsWith(" "));
		System.out.println(s2.endsWith("L"));

	}

}
