package com.stringmethod;

public class Trim {

	public static void main(String[] args) {
		String s="Nitesh";
		String s1=" Kumar";
		String s2=" Hello world ";
		System.out.println("Before using trim method the length of s is"+" "+s.length());
		System.out.println(s.trim());
		System.out.println("After using trim method the length of s is"+" "+s.length());
		System.out.println("Before using trim method the length of s1 is"+" "+s1.length());
		System.out.println(s1.trim());
		System.out.println("After using trim method the length of s1 is"+" "+s1.length());
		System.out.println("Before using trim method the length of s2 is"+" "+s2.length());
		System.out.println(s2.trim());
		System.out.println("After using trim method the length of s2 is"+" "+s2.length());

	}

}
